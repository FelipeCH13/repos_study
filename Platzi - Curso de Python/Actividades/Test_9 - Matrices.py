## Uso de matrices
matriz = [[1,2,3],
          [4,5,6],
          [7,8,9]]
print(matriz)
print(type(matriz))
## Acceder a un elemento de la matriz
print(matriz[0])

## Uso de tuplas
tupla = (1,2,3,4,5)
print(tupla)
print(type(tupla))
## Acceder a un elemento de la tupla
print(tupla[0])
